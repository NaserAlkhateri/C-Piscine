#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;

	c = 48;
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	else if (nb < 0)
	{
		nb *= -1;
		write(1, "-", 1);
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		c += nb;
		write(1, &c, 1);
	}
}
